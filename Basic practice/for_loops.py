#for loop for strings

group_names = ['Heather' , 'Jason' , 'Ashley']
for group_name in group_names:
    print('Member:', group_name)

#for loop for integers

for i in [1,6,9]:
    if i==1:
        print(i)
    if i==6:
        print('The number is 6')
    if i==9:
        print('Program is finished')